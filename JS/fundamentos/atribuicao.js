const a = 7  // temos aqui uma atribuição
let b = 3 

// atribuições usando atalhos  _ atribuições misturadas com operadores aritméticos

b+=a // atribuição aditiva o que equivale b = b + a
b -= 4  // atribuição subtrativa b = b - 4
console.log('B = '+b)

console.log('B = '+b)
b*=2 // b = b * 2  __ atribuição multiplicativa
console.log('B = '+b)

b /= 2 // b  = b /2 atribuição divisiva
console.log('B = '+b)

b %= 2//atribuição modular, equivale a b = b % 2
console.log('B = '+b)
